/**
 * A general interface for message-handlers.
 */
public interface MessageHandler {
	
	/**
	 * This method will receive messages and must be implemented. The message is received in String form.
	 *
	 * @param message the received message
	 * @param connectorId the id of the connector, which sent the message
	 */
	public void handleMessage(String message, int connectorId);

}
